#include <iostream>
using namespace std;

int main() {
    int num1, num2, sum;

    // Asking for user input
    cout << "Enter the first numbers: ";
    cin >> num1;

    cout << "Enter the second number: ";
    cin >> num2;

    // Calculating the sum
    sum = num1 + num2;

    // Displaying the result
    cout << "The sum of " << num1 << " and " << num2 << " is " << sum << endl;

    return 0;
}


